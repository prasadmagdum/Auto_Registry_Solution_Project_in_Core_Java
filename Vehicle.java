 public class Vehicle { 
  
 public int regId,price; 
 public String ownername,modelno,colour,city; 
 public long adhaarno,mobno; 
  
 public Vehicle(int regId, int price, String ownername, String modelno, String colour, String city, 
long adhaarno,long mobno) 
 { 
  super(); 
  this.regId = regId; 
  this.price = price; 
  this.ownername = ownername; 
  this.modelno = modelno; 
  this.colour = colour; 
  this.city = city; 
  this.adhaarno = adhaarno; 
  this.mobno = mobno; 
 } 
} 

